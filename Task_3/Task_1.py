print("Яблока для школярів")

# Поля для введення
numsho = int(input("Введіть кількість школярів:"))
numappl = int(input("Введіть загальну кількість яблок:"))

result2 = numappl // numsho # Дізнаемось кількість яблок кожному учню
result = numappl % numsho # Дізнаємось кількість яблук яка залишилась в корзинці.

print(f"Кожному школяру дісталося яблук: {result2}")
print(f"Яблук залишилось в корзинці: {result}")
